    class asynctask extends AsyncTask<Integer, Integer, Integer> {

        protected void onPreExecute(){
            pb.setProgress(cnt);
        }

        protected Integer doInBackground(Integer ... values){

            while (th_loop) {

                if (th_check) {

                    if (cnt == 5000) {

                        Auto_check(); // 자동로그인 체크하고 화면 넘어가게하고
                        th_loop = false; // 스레드는 종료하기
                    }
                    cnt += 10;
                    publishProgress(cnt);
                    try {
                        Thread.sleep(10);
                    } catch (InterruptedException e) {
                    }

                } else { }
            }

            return value;
        }

        protected void onProgressUpdate(Integer ... values) {
            pb.setProgress(cnt);
        }

        protected void onPostExecute(Integer result) {
        }

    }

    public void Auto_check(){

        if(sharePref.getBoolean("Auto_Login",false)){ // 자동로그인인 경우
            Intent intent = new Intent(getApplicationContext(),MusicChoAT.class);
            intent.putExtra("ID",sharePref.getString("Auto_Login_ID","")); // ID값 넘겨주기

            startActivity(intent); // 다음 화면으로 넘어간다
            check = true; // 화면넘어간거야 !
            finish();
        }
        else{ // 자동로그인이 아니거나 아예 없거나
            Intent intent = new Intent(getApplicationContext(),LoginAT.class);
            startActivity(intent); // 다음 화면으로 넘어간다
            check = true; // 화면넘어간거야 !
            finish();
        }
    }

    protected void onStart(){
        super.onStart();
        th_check = true;  // 스레드 시작
        Log.e(tag,"onStart");
    }

    protected void onResume(){
        super.onResume();
        Log.e(tag,"onResume");
    }

    protected void onPause(){
        super.onPause();
        th_check = false; // 스레드 쉬어라
        Log.e(tag,"onPause");
    }

    protected void onStop() { // 나갈 때
        super.onStop();
        Log.e(tag,"onStop");
        if(!check) {
            ms.Music_pause();
        }
    }

    protected void onDestroy() { // 꺼질 때
        super.onDestroy();
        Log.e(tag,"onDestroy");
        th_loop = false; // 스레드 종료
        unbindService(conn); // 서비스 종료
    }

    protected void onRestart() { // 다시 들어올 때
        super.onRestart();
        check = false;
        Log.e(tag,"onRestart");
        ms.Music_on();

    }
